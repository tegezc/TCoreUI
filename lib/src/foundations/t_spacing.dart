/// Standar jarak (margin/padding) untuk menjaga konsistensi UI.
abstract class TSpacing {
  /// 4.0 - Jarak sangat kecil (misal: antara icon dan teks di dalam button)
  static const double xs = 4.0;

  /// 8.0 - Jarak kecil (misal: antar item di dalam list/row)
  static const double sm = 8.0;

  /// 16.0 - Jarak standar (misal: padding default untuk layar atau card)
  static const double md = 16.0;

  /// 24.0 - Jarak agak besar (misal: jarak antar section/kelompok data)
  static const double lg = 24.0;

  /// 32.0 - Jarak besar (misal: margin bawah halaman sebelum tombol CTA)
  static const double xl = 32.0;

  /// 48.0 - Jarak sangat besar
  static const double xxl = 48.0;
}